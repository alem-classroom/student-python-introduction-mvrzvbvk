def add(first, second):
    # return sum of first and second arguments
    ans = first + second
    return ans

def sub(first, second):
    # return subtraction of first and second arguments
    ans = first - second
    return ans

def multiply(first, second):
    # return multiplicatiom of first and second arguments
    ans = first * second
    return ans

def div_with_floor(first, second):
    # return division with floor of first and second arguments
    ans = first//second
    return ans


def div_without_floor(first, second):
    # return division without floor of first and second arguments
    ans = first/second
    return ans

